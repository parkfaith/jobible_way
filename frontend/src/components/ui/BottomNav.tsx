import { useLocation, useNavigate } from 'react-router-dom'

const NAV_ITEMS = [
  { path: '/home', label: '홈', icon: HomeIcon },
  { path: '/weeks', label: '주차', icon: BookIcon },
  { path: '/daily', label: '체크', icon: CheckIcon },
  { path: '/progress', label: '진도', icon: ChartIcon },
  { path: '/profile', label: '설정', icon: UserIcon },
]

export default function BottomNav() {
  const location = useLocation()
  const navigate = useNavigate()

  return (
    <nav className="fixed bottom-0 left-1/2 -translate-x-1/2 w-full max-w-[768px] bg-[var(--color-surface)] border-t border-[var(--color-border)] z-50 pb-[env(safe-area-inset-bottom)]">
      <div className="flex items-center justify-around h-16">
        {NAV_ITEMS.map((item) => {
          const isActive = location.pathname.startsWith(item.path)
          return (
            <button
              key={item.path}
              onClick={() => navigate(item.path)}
              className={`flex flex-col items-center gap-1 px-3 py-1 transition-colors cursor-pointer ${
                isActive ? 'text-[var(--color-accent)]' : 'text-[var(--color-text-secondary)]'
              }`}
            >
              <item.icon className="w-5 h-5" />
              <span className="text-[10px] font-[var(--font-ui)]">{item.label}</span>
            </button>
          )
        })}
      </div>
    </nav>
  )
}

